#Python Program to implement Bitwise Operations
a=int(input("Enter a value: "))
b=int(input("Enter b value: "))
print(f"Bitwise AND is {a&b}")
print(f"Bitwise OR is {a|b}")
print(f"Exclusive OR is {a^b}")
print(f"Shift Left is {a<<b}")
print(f"Shift Right is {a>>b}")
print(f"Complement is {~a}")
