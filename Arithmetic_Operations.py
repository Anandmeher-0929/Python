#Python Program to implement Arithmetic Operations
a=int(input("Enter a value: "))
b=int(input("Enter b value: "))
print("_____________________")
add= a+b
print(f"\nAddition : {add:}\n")
sub = a-b
print(f"Subtraction : {sub:}\n")
multi = a*b
print(f"Multiplication: {multi:}\n")
div = a/b
print(f"Division: {div:}\n")
mod = a%b
print(f"Modulus: {mod:}\n")
floor_mod = a//b
print(f"Floor Modulus: {floor_mod:}\n")
exp = a**b
print(f"Exponentiation : {exp:}\n")